package Basics;

public class ArithmaticOporatins2 {
    public static void main(String[] args) {
        double x = 3;
        System.out.println(x);
        double y = 2;
        // + - * / %

        System.out.println(x+y); // 5.0
        System.out.println(x-y); // 1.0
        System.out.println(x*y); // 6.0
        System.out.println(x/y); // 1.5
        System.out.println(x%y); // 1.0
    }
}
