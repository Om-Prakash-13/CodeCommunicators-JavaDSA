package M1_Basics;

public class VolumeOfASphere {
    public static void main(String[] args) {
        // volume of the sphere = (4/3) * pi * r * r * r
        double pi = Math.PI; // 3.141.....
        double r = 4.3;
        double volume = (4.0/3) * pi * r * r * r;
        System.out.println("Volume of the sphere: "+volume);
    }
}
