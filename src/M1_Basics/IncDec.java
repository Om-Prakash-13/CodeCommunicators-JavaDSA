package M1_Basics;
public class IncDec {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x); // 7
        x++; //x = x + 1;
        System.out.println(x); // 8
        x--; //x = x - 1;
        System.out.println(x); // 7

        int y = 5;
        ++y; // x = x + 1;
        System.out.println(y); // 6
        --y; // x = x - 1;
        System.out.println(y); // 5
    }
}
