package M1_Basics;

public class AreaOfACircle {
    public static void main(String[] args) {
        // area of circle = pi * r * r
        float pi = 3.141f;
        double r = 5.3;
        double area = pi * r *r;
        System.out.print("Area of the circle is: "+area);
        System.out.println(area);
    }
}
