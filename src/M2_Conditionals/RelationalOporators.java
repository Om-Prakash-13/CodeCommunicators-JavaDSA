package M2_Conditionals;

public class RelationalOporators {
    public static void main(String[] args) {
        int x = 5;
        int y = 4;
        System.out.println(x == y);
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y);
        System.out.println(x >= y);
        System.out.println(x != y);
    }
}
